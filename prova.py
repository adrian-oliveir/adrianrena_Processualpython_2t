chamada = ["--1Adrian--", "Angelo-", "Arthur", "25 Raphael"]
# Define a largura padrão baseada no nome mais longo
largura = max(len(nome) for nome in chamada) + 4

# Linha superior
linha_topo = "".join("+" + "-" * largura for _ in chamada) + "+"
# Linha do meio com os nomes centralizados
linha_meio = "".join("|" + nome.center(largura) for nome in chamada) + "|"
# Linha inferior
linha_base = "".join("+" + "-" * largura for _ in chamada) + "+"

# Exibe o quadro
print(linha_topo)
print(linha_meio)
print(linha_base)

